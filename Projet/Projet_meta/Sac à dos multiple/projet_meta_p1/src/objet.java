


public class objet {

	int valeur;
	int poids;
	public objet(int val,int poids){
		this.valeur=val;
		this.poids=poids;
	}
	public int getValeur() {
		return valeur;
	}
	public void setValeur(int valeur) {
		this.valeur = valeur;
	}
	public int getPoids() {
		return poids;
	}
	public void setPoids(int poids) {
		this.poids = poids;
	}
}
